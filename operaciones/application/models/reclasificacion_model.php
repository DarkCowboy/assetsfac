        $valida = $this->db->get_where('reclasificaciones', array('id_pj' => $form['id_pj']))->row_array();
        if(count($valida)>0){
            $this->db->where('id_pj', $form['id_pj']);
            $this->db->update('reclasificaciones', $form);
        }else{
            $this->db->insert('reclasificaciones', $form);
        }
    }
    
    public function get_reclasificacion($id_pj){
        return $this->db->get_where('reclasificaciones', array('id_pj' => $id_pj))->row_array();
